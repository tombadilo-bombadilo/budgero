/**
 * Compiles Budgero's user-facing docs + privacy policy (which live in
 * packages/website) into a generated TS module the in-app AI assistant can
 * search via the search_docs tool. Run via `pnpm build:knowledge` (also wired
 * into predev/prebuild). Commit the generated output so CI/builds don't need
 * the website package present.
 */
import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const websiteDir = join(here, '..', '..', 'website');
const docsDir = join(websiteDir, 'content', 'docs');
const privacyFile = join(websiteDir, 'src', 'app', 'privacy', 'page.tsx');
const outDir = join(here, '..', 'src', 'features', 'ai', 'lib', 'knowledge');
const outFile = join(outDir, 'knowledge-base.generated.ts');

const HTML_ENTITIES = {
  '&apos;': "'",
  '&rsquo;': '’',
  '&lsquo;': '‘',
  '&ldquo;': '“',
  '&rdquo;': '”',
  '&mdash;': '—',
  '&ndash;': '–',
  '&amp;': '&',
  '&nbsp;': ' ',
  '&lt;': '<',
  '&gt;': '>',
  '&quot;': '"',
  '&hellip;': '…',
};

function decodeEntities(text) {
  let out = text;
  for (const [entity, char] of Object.entries(HTML_ENTITIES)) {
    out = out.split(entity).join(char);
  }
  return out;
}

/** Parse leading YAML-ish frontmatter (--- ... ---). Returns {data, body}. */
function parseFrontmatter(raw) {
  if (!raw.startsWith('---')) return { data: {}, body: raw };
  const end = raw.indexOf('\n---', 3);
  if (end === -1) return { data: {}, body: raw };
  const fm = raw.slice(3, end).trim();
  const body = raw.slice(end + 4).trim();
  const data = {};
  for (const line of fm.split('\n')) {
    const m = line.match(/^([A-Za-z0-9_]+):\s*(.*)$/);
    if (m) data[m[1]] = m[2].replace(/^["']|["']$/g, '').trim();
  }
  return { data, body };
}

function cleanDocBody(body) {
  return body
    .split('\n')
    .filter((l) => !/^\s*import\s+/.test(l)) // drop MDX import lines
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function loadDocs() {
  if (!existsSync(docsDir)) return [];
  return readdirSync(docsDir)
    .filter((f) => f.endsWith('.mdx'))
    .map((file) => {
      const raw = readFileSync(join(docsDir, file), 'utf8');
      const { data, body } = parseFrontmatter(raw);
      const id = file.replace(/\.mdx$/, '');
      return {
        id,
        title: data.title || id,
        summary: data.summary || '',
        section: data.section || 'docs',
        body: cleanDocBody(body),
      };
    });
}

/** Strip JSX/TSX down to readable prose for the privacy policy page. */
function loadPrivacy() {
  if (!existsSync(privacyFile)) return null;
  const raw = readFileSync(privacyFile, 'utf8');
  const start = raw.indexOf('return (');
  if (start === -1) return null;
  let text = raw.slice(start);

  text = text
    .replace(/\{'\s*'\}/g, ' ') // {' '}
    .replace(/className="[^"]*"/g, '')
    .replace(/<(p|h1|h2|h3|h4|li|tr|div|hr|br|\/p|\/h1|\/h2|\/h3|\/h4|\/li|\/tr|\/div)[^>]*>/gi, '\n')
    .replace(/<[^>]+>/g, '') // remaining tags
    .replace(/\{[^}]*\}/g, ''); // stray JSX expressions

  text = decodeEntities(text)
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean)
    // Drop leftover JSX scaffolding lines (return (, ), );, }, ( ).
    .filter((l) => !/^(return\s*\(|\)\s*;?|\}|\()$/.test(l))
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();

  return {
    id: 'privacy-policy',
    title: 'Privacy Policy',
    summary: "How Budgero collects, uses, retains, and shares data; your rights under GDPR/CCPA.",
    section: 'legal',
    body: text,
  };
}

const docs = [];
const privacy = loadPrivacy();
if (privacy) docs.push(privacy);
docs.push(...loadDocs());

if (docs.length === 0) {
  // Don't clobber the committed bundle when the website package isn't available
  // (e.g. a self-host build of just packages/app).
  console.warn('[build-knowledge] No docs found — keeping existing bundle. Is packages/website present?');
  process.exit(0);
}

mkdirSync(outDir, { recursive: true });
const header = `/* eslint-disable */
// AUTO-GENERATED by scripts/build-knowledge.mjs — do not edit by hand.
// Source: packages/website docs + privacy policy. Regenerate with: pnpm build:knowledge

export interface KnowledgeDoc {
  id: string;
  title: string;
  summary: string;
  section: string;
  body: string;
}

export const KNOWLEDGE_DOCS: KnowledgeDoc[] = ${JSON.stringify(docs, null, 2)};
`;
writeFileSync(outFile, header, 'utf8');
console.log(`[build-knowledge] Wrote ${docs.length} docs to ${outFile}`);
